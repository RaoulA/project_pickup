			</div>

			<h3>Wenn ich die App an Dritte über die bereitgestellte Funktion weiterempfehle, hat Valora nachher die Daten (Handynummer) des Dritten?</h3>
			<div>
				<p>Valora verwendet diese Daten (Mobiltelefonnummer) ausschliesslich f&uuml;r die Abwicklung des Weiterempfehlungsvorgangs &uuml;ber die App-Funktion. Die Daten werden w&auml;hrend eines Monats in deinem Auftrag gespeichert, damit Du bei einer allf&auml;lligen Registrierung dieser beworbenen Person innerhalb dieser Zeit von einem Reward profitierst. Jegliche Analyse oder Nutzung f&uuml;r andere Zwecke wie z.B. Werbung ist ausgeschlossen. Diese Daten werden in jedem Fall drei Monate nach Erhebung gel&ouml;scht und werden zu keinem Zeitpunkt an Dritte weitergegeben.</p>

			</div>

