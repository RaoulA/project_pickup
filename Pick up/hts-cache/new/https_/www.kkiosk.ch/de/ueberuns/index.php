	<div class="contentTitle standardSpacing">
		<h2>Neueröffnung: Der k kiosk ist zurück in Affoltern am Albis</h2>
	</div>

	<div class="contentText noStyle standardSpacing">
		<div class="text">
			<p>Die Anwohner von Affoltern am Albis bedauerten die Schliessung des k kiosks vor knapp einem Jahr. Umso gr&ouml;sser war die Freude am 28. Juli 2017: In neuartiger Ausstattung, mit einem noch breiterem Sortiment und dem altbekannten Team kehrt der k kiosk jetzt in die Gemeinde zur&uuml;ck.<br />
Frischer Kaffee und ein Gipfeli f&uuml;r unterwegs? Eine Glace zwischendurch oder ein Sandwich &uuml;ber den Mittag? Das alles ist in der neuer&ouml;ffneten k kiosk Verkaufsstelle in Affoltern am Albis erh&auml;ltlich. Im September 2016 kam es wegen des Umzugs der Migros-Filiale, in welcher der k kiosk eingemietet war, zur Schliessung der Verkaufsstelle. Aufgrund vieler Kundenreaktionen suchte Valora seither intensiv nach einem neuen Standort. Ende Juni konnte nun nach nur 10 Monaten der neue <a href="https://www.kkiosk.ch/de/allover/standortsuche/" target="_blank">k kiosk mitten im Zentrum des Oberdorfs</a> er&ouml;ffnet werden.</p>

<p><strong>Neuer Auftritt mit eingespieltem Team</strong><br />
Die neue Verkaufsstelle erscheint in neuem Gewand. Neben Tabakwaren und Presse sind neu mehr frische Produkte f&uuml;r den Sofortverzehr erh&auml;ltlich. Zudem gibt es 12 Sitzpl&auml;tze vor dem Kiosk, ein guter Ort f&uuml;r einen gem&uuml;tlichen Kaffee oder um einen Lottoschein auszuf&uuml;llen. Was bleibt, sind die vertrauten Gesichter hinter der Verkaufstheke: ein Grossteil des Teams und die Gesch&auml;ftsf&uuml;hrerin des ehemaligen k kiosks sind wieder mit an Bord.</p>

<p><strong>Neue k kiosk App</strong><br />
Kundinnen und Kunden profitieren zudem neu auch von der praktischen k kiosk App. Diese kann einfach auf das Mobiltelefon geladen werden und schon profitieren die Kunden von attraktiven Promotionen und Rabatt-Gutscheinen, die am k kiosk eingel&ouml;st werden k&ouml;nnen. Auch sind damit die bisherigen Stempelkarten digital verf&uuml;gbar und die Lieblingsprodukte sind so bereits beim 6. Einkauf gratis.</p>

