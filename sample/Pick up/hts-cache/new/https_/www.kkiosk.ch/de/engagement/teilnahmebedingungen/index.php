		<h1>Teilnahmebedingungen Wettbewerb «Festival Locarno»</h1>
	</div>

	<div class="contentText lead lineSpacing">
		<div class="text">
			<p>Veranstalterin des Wettbewerbs ist Valora Schweiz AG, Hofackerstrasse 40, 4132 Muttenz, Schweiz (Valora).</p>

		</div>
	</div>

	<div class="contentText noStyle standardSpacing">
		<div class="text">
			<p>Teilnahmeberechtigt am Gewinnspiel der Valora Schweiz AG sind Personen mit Wohnsitz in der Schweiz. Mitarbeitende von Unternehmen in der Valora Gruppe sowie der angeschlossenen Agentur und Franchisebetriebe sind von der Teilnahme ausgeschlossen. Die Teilnehmenden erkl&auml;ren sich ausdr&uuml;cklich damit einverstanden, dass die Personendaten, welche online f&uuml;r die Wettbewerbsteilnahme bekannt geben werden, f&uuml;r die Durchf&uuml;hrung des Wettbewerbs und zu Marketingzwecken von Unternehmen der Valora Gruppe verwendet werden k&ouml;nnen. Sie k&ouml;nnen die Verwendung ihrer angegebenen Daten jedoch jederzeit schriftlich ablehnen. Daten werden vertraulich behandelt. Die online-Teilnahme ist bis am 7.7.2017 23:59 Uhr m&ouml;glich.<br />
Mehrfachteilnahmen sind unzul&auml;ssig. Automatisch generierte Teilnahmen werden vom Wettbewerb ausgeschlossen. Die Gewinner der 10 x 2 Tickets f&uuml;r das Festival Locarno werden aus allen Einsendungen gelost. Die Gewinner werden bis zum 20.7.2017 via Zustellung der Tickets &uuml;ber den Gewinn informiert. Es besteht kein Kaufzwang. Der Rechtsweg ist ausgeschlossen. Es wird keine Korrespondenz gef&uuml;hrt. Keine Barauszahlung m&ouml;glich. Der Wettbewerb unterliegt schweizerischem Recht und Gerichtsstand ist Arlesheim, BL.</p>

<p>Die Teilnehmer geben bei der Teilnahme an, f&uuml;r welchen Tag sie im Falle eines Gewinnes ein Tagesticket w&uuml;nschen:</p>

<p>&nbsp;&nbsp;&nbsp; Mi, 02. August 2017<br />
&nbsp;&nbsp;&nbsp; Do, 03. August 2017<br />
&nbsp;&nbsp;&nbsp; Fr, 04. August 2017<br />
&nbsp;&nbsp;&nbsp; Sa, 05. August 2017<br />
&nbsp;&nbsp;&nbsp; So, 06. August 2017<br />
&nbsp;&nbsp;&nbsp; Mo, 07. August 2017<br />
&nbsp;&nbsp;&nbsp; Di, 08. August 2017<br />
&nbsp;&nbsp;&nbsp; Mi, 09. August 2017<br />
&nbsp;&nbsp;&nbsp; Do, 10. August 2017<br />
&nbsp;&nbsp;&nbsp; Fr, 11. August 2017<br />
&nbsp;&nbsp;&nbsp; Sa, 12. August 2017</p>

<p>Valora wird die Preise an den jeweiligen Gewinner auf dem Postweg und zwar nur an die in der Schweiz angegebene Adresse zustellen. Eine Barauszahlung der Gewinne ist ausgeschlossen.</p>

