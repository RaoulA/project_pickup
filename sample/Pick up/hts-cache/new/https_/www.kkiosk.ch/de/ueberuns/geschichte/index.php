	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">k kiosk wird 80 Jahre alt</h3>
		<div class="text">
			<div class="text">
<p>2014 feiert k kiosk sein 80 Jahre Jubil&auml;um.</p>
</div>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">k kiosk in Deutschland und Luxemburg</h3>
		<div class="text">
			<p>Nach dem Rebranding von &uuml;ber 70 Verkaufsstellen auf k kiosk in Luxemburg in 2009 schafft Valora Retail Deutschland 2010 einen internationalen Wiedererkennungswert mit der Einf&uuml;hrung von k kiosk in Deutschland. Non Travel-Standorte tragen nun die in der Schweiz seit langem etablierte k kiosk Marke. 2011 und 2012 folgt die Umwandlung ehemaliger tabacon-Stores in moderne Kioske nach Schweizer Vorbild.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">k kiosk: Neue Markenstrategie</h3>
		<div class="text">
			<p>2004 beginnt die Umsetzung der neuen Markenstrategie von k kiosk. k kiosk ist ein komplett &uuml;berarbeitetes und weiter optimiertes Verkaufskonzept, mit einem neuen Look. Mit der Neupositionierung will sich der k kiosk klar von seinen Mitbewerbern im Kiosk- und kleinfl&auml;chigen Shopgesch&auml;ft differenzieren. Im Fr&uuml;hsommer 2006 ist die Umstellung des Auftritts von rot-weiss auf die Markenfarben blau-weiss-rot erfolgt.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Konzernstrukturen – Fokus auf das Kerngeschäft</h3>
		<div class="text">
			<p>Der Konzern beschliesst die Fokussierung auf das Kerngesch&auml;ft. Mit der Vereinfachung der Konzernstrukturen per 1. Januar 03 wurden aus den beiden Kiosksparten k GROUP (Schweiz) und (International) die Marktdivisionen Valora Retail und Valora Wholesale &ndash; die bisherige L&auml;nderaufteilung wurde aufgehoben. Und gesch&auml;ftsseitig wurden u.a. die <a href="http://www.merkur.ch/" target="_blank">Merkur Confiserien</a> verkauft. 2005 bringt die Fokussierung auf das Kerngesch&auml;ft weitere Anpassungen in der Organisationsstruktur. 2010 sind in Muttenz die f&uuml;r den Einzelhandel (Valora Retail: mit Kiosk, Convenience, Gastronomie) sowie den Vertrieb von Presseerzeugnissen &amp; Grosshandelsaktivit&auml;ten&nbsp;(Valora Services) zust&auml;ndigen Divisionen angesiedelt.<br />
Per 1. Januar 2014 &uuml;bernimmt Thomas Kirschner, der Mehrheitsgesellschafter des f&uuml;hrenden deutschen Pressegrossisten PVG von der Valora Gruppe die Division &laquo;Valora Services.&raquo; <a href="http://7days-media.ch" target="_blank">7 Days Media Services AG</a> heisst das neue Unternehmen, welches das komplette Pressgrosshandelsgesch&auml;ft in der deutsch- und italienischsprachigen Schweiz &uuml;bernimmt.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Anerkennung der Branche Kiosk</h3>
		<div class="text">
			<p>Im Fr&uuml;hling 2000 bewilligte das Bundesamt f&uuml;r Berufsbildung und Technologie die Branche Kiosk &ndash; und erm&ouml;glichte damit, dass im August 2001 erstmals Lehrlinge am Kiosk ihre Verkaufslehre beginnen konnten.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Valora</h3>
		<div class="text">
			<p>1996 erhielt der Konzern Merkur einen neuen Namen: Valora. 1998 f&uuml;hrte Valora ihre beiden Marktsparten Merkur (Confiserie/ Gastronomie) und Kiosk in der Sparte k GROUP (Schweiz) zusammen, deren Kerngesch&auml;ft der Einzelhandel, Grosshandel und die Logistik war.</p>

<p>2001 wurde die Kiosk AG auf k Kiosk AG, 2004 auf Valora AG und dann Valora Schweiz AG umfirmiert.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Expansion von Merkur</h3>
		<div class="text">
			<p>1989/90 kaufte die Firma Merkur (Merkur war 1905 als Schweizer Schokoladen und Colonialhaus gegr&uuml;ndet worden) die Kioskfirmen Schmidt-Agence AG und Kiosk AG. Die Schmidt-Agence verf&uuml;gte &uuml;ber rund 1&#39;100 eigene Verkaufsstellen und belieferte 300 Depots. Die Kiosk AG betrieb 470 eigene Kioske und hatte 1&#39;250 Depotkunden. 1991 fusionierten sie zur neuen Kiosk AG, die 1994 in das soeben fertig gebaute sogenannte Handelshaus Muttenz einzog.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Festigung des Kioskmarktes</h3>
		<div class="text">
			<p>Der Kioskmarkt in seiner heutigen Bedeutung wurde in den 50er Jahren gefestigt. Die Kiosk AG sicherte sich einen Teil des Grosshandel-Gesch&auml;ftes mit Presselieferungen an Coop Verkaufsstellen in der Deutschschweiz. Die Schmidt-Agence AG er&ouml;ffnete dagegen in Migros-M&auml;rkten eigene Verkaufsstellen.</p>

<p>Auf Bundesebene kam es 1973 und 1983 zu wichtigen Entscheiden f&uuml;r die Kioskfirmen. Das Eidgen&ouml;ssische Kartellgesetz von 1973 schaltete das Abwerben bestehender Verkaufsstellen aus, was eine Sicherung des bestehenden Gesch&auml;ftsvolumen bedeutete. 1983 anerkannte das Bundesgericht die Besonderheit des Pressevertriebs. Es entschied, dass der Verlag Eigent&uuml;mer der Presse bis zum Verkauf bleibe, damit das volle Risiko trage, und entsprechend seine Vertriebswege selbst bestimmen k&ouml;nnen.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Schweizerischer Kioskinhaber-Verband</h3>
		<div class="text">
			<p>Ein entscheidender Erfolgsfaktor f&uuml;r den Kiosk ist der Standort - entsprechend tobte ein Konkurrenzkampf um die besten Passantenlagen. Zur Beruhigung des Marktes teilten die SBB ihr Pachtgebiet in Absprache mit den Privatbahnen unter den Kioskfirmen auf. Weil die Einzelh&auml;ndler auch Pachtgebiet der Bahnen begehrten, gingen sie in die Opposition und gr&uuml;ndeten 1926 den Schweizerischen Kioskinhaber-Verband SKIV.</p>

		</div>
	</div>

	<div class="contentText noStyle lineSpacing">
		<h3 class="sectionTitle">Pressevertrieb</h3>
		<div class="text">
			<p>Mit sich ver&auml;ndernden Lesegewohnheiten erhielt die Presse gegen&uuml;ber dem Buch Oberhand. Nebst dem Abonnement war der Kiosk der wichtigste Vertriebskanal f&uuml;r die Presseerzeugnisse. Unter anderem wegen der politischen Bedeutung der Berichterstattung, &quot;der Information der Bev&ouml;lkerung durch die Presse&quot; profitierte der Kiosk von weniger restriktiven Laden&ouml;ffnungszeiten. Es ergab sich das heute noch geltende Vertriebsprinzip zwischen Verlag und Handel. Bei ihrem Kommissionsgesch&auml;ft sind die Verkaufspreise vom Verlag her verbindlich (vorgeschrieben, aufgedruckt). Die Ware bleibt bis zum Verkauf Eigentum des Verlages.</p>

		</div>
	</div>

	<div class="contentText noStyle standardSpacing">
		<h3 class="sectionTitle">Kioskgeschäft</h3>
		<div class="text">
			<p>1872 entstand die Buchhandlung F. Festersen &amp; Cie, 1913 in Paul Schmidt Zeitungsagentur (Basel) umbenannt, die sich mit dem Vertrieb von Zeitschriften vorwiegend deutscher Herkunft befasste. 1883 wurde in Bern unter der F&uuml;hrung von Samuel Payot die SA Librairie Edition gegr&uuml;ndet. Sie war u.a. Konzession&auml;rin von SBB-Bahnhofs-Buchhandlungen und betrieb &quot;Kiosques &agrave; Journaux&quot; in Bern, Z&uuml;rich und Biel. 1934 erfolgte die Umbenennung in Kiosk AG. 1913 gr&uuml;ndete Lucien Naville in Genf mit Beteiligung der Hachette, Vertriebsfirma der franz&ouml;sischen Verlage, das Depot Central de Librairie SA (die sp&auml;tere Naville). 1914 erweiterte er unter dem Namen Allgemeine Zeitung-Expedition Dornacherstrasse (Azed AG) sein Depotgesch&auml;ft bis nach Basel. Die italienischen Verlagsh&auml;user verhielten sich wie die franz&ouml;sischen. Der Corriere della Sera gr&uuml;ndete beispielsweise 1939 die Verteilfirma Melisa SA in Lugano f&uuml;r den Vertrieb italienischer Presseerzeugnisse.</p>

		</div>
